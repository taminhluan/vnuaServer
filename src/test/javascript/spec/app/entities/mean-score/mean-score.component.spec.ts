import { ComponentFixture, TestBed } from '@angular/core/testing';
import { of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, Data } from '@angular/router';

import { VnuaServerTestModule } from '../../../test.module';
import { MeanScoreComponent } from 'app/entities/mean-score/mean-score.component';
import { MeanScoreService } from 'app/entities/mean-score/mean-score.service';
import { MeanScore } from 'app/shared/model/mean-score.model';

describe('Component Tests', () => {
  describe('MeanScore Management Component', () => {
    let comp: MeanScoreComponent;
    let fixture: ComponentFixture<MeanScoreComponent>;
    let service: MeanScoreService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [VnuaServerTestModule],
        declarations: [MeanScoreComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: {
                subscribe: (fn: (value: Data) => void) =>
                  fn({
                    pagingParams: {
                      predicate: 'id',
                      reverse: false,
                      page: 0
                    }
                  })
              }
            }
          }
        ]
      })
        .overrideTemplate(MeanScoreComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(MeanScoreComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(MeanScoreService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new MeanScore(123)],
            headers
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.meanScores && comp.meanScores[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new MeanScore(123)],
            headers
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.meanScores && comp.meanScores[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should calculate the sort attribute for an id', () => {
      // WHEN
      comp.ngOnInit();
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,desc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // INIT
      comp.ngOnInit();

      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,desc', 'id']);
    });
  });
});
